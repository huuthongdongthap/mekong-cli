# Cascade state per pane: tracks what needs to happen next
# Values: "needs_bootstrap" | "needs_portfolio" | "ready_operate" | "needs_fix"

get_pane_state() {
  local PANE=$1
  local STATE_FILE="$PANE_STATE_DIR/pane_${PANE}_state"
  cat "$STATE_FILE" 2>/dev/null || echo "ready_operate"
}

set_pane_state() {
  local PANE=$1
  local STATE=$2
  echo "$STATE" > "$PANE_STATE_DIR/pane_${PANE}_state"
}

# Analyze last 45 lines for cascade triggers
# Returns: "bootstrap" | "fix" | "analysis_done" | "continue"
analyze_output() {
  local OUTPUT="$1"
  local OUTPUT_LOWER=$(echo "$OUTPUT" | tr '[:upper:]' '[:lower:]')

  # Priority 1: SUCCESS signals ‚Üí project is ready, continue rotation
  if echo "$OUTPUT_LOWER" | grep -qE "‚úÖ|bootstrap complete|ready for|initialized|success|completed|saved|created|done|finished|launched|deployed|operational"; then
    # CASCADE BRIDGE: detect if output is analysis/report (not code)
    # If last command produced analysis ‚Üí trigger engineering /cook next
    if echo "$OUTPUT_LOWER" | grep -qE "report saved|analysis complete|terrain.*score|thesis.*updated|momentum.*score|five.factor|market.*analysis|content.*plan"; then
      echo "analysis_done"
      return
    fi
    echo "continue"
    return
  fi

  # Priority 2: Critical errors ‚Üí needs fix
  if echo "$OUTPUT_LOWER" | grep -qE "error:|failed:|exception|traceback|crashed|fatal:"; then
    echo "fix"
    return
  fi

  # Priority 3: Not initialized ‚Üí needs bootstrap
  if echo "$OUTPUT_LOWER" | grep -qE "not initialized|does not exist|not configured|no studio found|missing portfolio|missing studio"; then
    echo "bootstrap"
    return
  fi

  # Default: continue rotation
  echo "continue"
}

# Get cascade command based on state and output analysis
get_cascade_command() {
  local PANE=$1
  local PROJECT=$2
  local DIR=$3
  local NAME=$4
  local OUTPUT="$5"

  local CURRENT_STATE=$(get_pane_state "$PANE")
  local ANALYSIS=$(analyze_output "$OUTPUT")

  # Log analysis to stderr (not captured in command output)
  echo "   üìä State='$CURRENT_STATE' Analysis='$ANALYSIS'" >&2

  # OVERRIDE: If analysis says fix/bootstrap/analysis_done, handle immediately
  if [ "$ANALYSIS" = "fix" ]; then
    set_pane_state "$PANE" "needs_fix"
    echo "/cook [CH·ªà project: ${PROJECT}] [DIR: ${DIR}] Fix errors in $NAME ‚Äî KH√îNG ƒë·ª•ng project kh√°c"
    return
  fi

  if [ "$ANALYSIS" = "bootstrap" ]; then
    set_pane_state "$PANE" "needs_bootstrap"
    echo "/studio-bootstrap [CH·ªà project: ${PROJECT}] [DIR: ${DIR}] $NAME ‚Äî KH√îNG ƒë·ª•ng project kh√°c"
    return
  fi

  # CASCADE BRIDGE: analysis ‚Üí code. This is the missing link!
  # When Studio/Business output analysis, AUTO-TRIGGER /cook to produce real code
  if [ "$ANALYSIS" = "analysis_done" ]; then
    echo "   üåâ CASCADE: analysis ‚Üí engineering /cook" >&2
    # Try brain learning for best command
    local BEST_CMD
    BEST_CMD=$(timeout 3 node -e "
      try {
        const r = require('$HOME/mekong-cli/apps/openclaw-worker/lib/factory-roi-calculator');
        const cmd = r.getBestCommand('$PROJECT', 'deployed');
        if (cmd) console.log(cmd); else console.log('');
      } catch(e) { console.log(''); }
    " 2>/dev/null || echo "")

    if [ -n "$BEST_CMD" ] && [ ${#BEST_CMD} -gt 3 ]; then
      echo "${BEST_CMD} [CH·ªà project: ${PROJECT}] [DIR: ${DIR}]"
    else
      # Default cascade: build feature based on analysis context
      echo "/cook Build the next feature for ${NAME} based on latest analysis. Read plans/reports/ for context. Write TypeScript code to src/. [CH·ªà project: ${PROJECT}] [DIR: ${DIR}]"
    fi
    return
  fi

  # State machine: chain commands logically
  case "$CURRENT_STATE" in
    "needs_bootstrap")
      # Just set bootstrap, next iteration will check output
      set_pane_state "$PANE" "needs_portfolio"
      echo "/studio-bootstrap [CH·ªà project: ${PROJECT}] [DIR: ${DIR}] $NAME ‚Äî KH√îNG ƒë·ª•ng project kh√°c"
      ;;
    "needs_portfolio")
      # Check if bootstrap succeeded
      if echo "$OUTPUT" | grep -qE "success|completed|saved"; then
        set_pane_state "$PANE" "ready_operate"
        echo "/portfolio-create [CH·ªà project: ${PROJECT}] [DIR: ${DIR}] $NAME ‚Äî KH√îNG ƒë·ª•ng project kh√°c"
      else
        # Bootstrap failed, retry
        echo "/studio-bootstrap [CH·ªà project: ${PROJECT}] [DIR: ${DIR}] $NAME ‚Äî KH√îNG ƒë·ª•ng project kh√°c"
      fi
      ;;
    "ready_operate")
      # Normal operation mode ‚Äî use rotation
      local ROTATION_CMD=$(get_next_command "$PANE" "$PROJECT" "$DIR" "$NAME")
      echo "$ROTATION_CMD"
      ;;
    *)
      # Unknown state ‚Üí bootstrap first
      set_pane_state "$PANE" "needs_bootstrap"
      echo "/studio-bootstrap $NAME ‚Äî Th∆∞ m·ª•c: ${DIR}"
      ;;
  esac
}

# ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
# PROJECT STATE DETECTION ‚Äî Checks actual files on disk
# States: empty ‚Üí scaffolded ‚Üí built ‚Üí deployed ‚Üí operating
