# Record a dispatched command
record_dispatch() {
  local PANE=$1 CMD=$2
  local CMD_HASH=$(echo "$CMD" | head -c 80 | md5 2>/dev/null || echo "$CMD" | head -c 80 | md5sum 2>/dev/null | cut -d' ' -f1)
  echo "$(date +%s)|P${PANE}|${CMD_HASH}|$(echo "$CMD" | head -c 60)" >> "$DISPATCH_HISTORY"
  # Prune entries older than TTL
  if [ -f "$DISPATCH_HISTORY" ]; then
    local CUTOFF=$(( $(date +%s) - DEDUP_TTL ))
    local TMP="${DISPATCH_HISTORY}.tmp"
    awk -F'|' -v cutoff="$CUTOFF" '$1 >= cutoff' "$DISPATCH_HISTORY" > "$TMP" 2>/dev/null && mv "$TMP" "$DISPATCH_HISTORY" || true
  fi
}

# Check if command was already dispatched to ANY pane in last 2h
# Returns 0 if duplicate (should skip), 1 if unique (safe to dispatch)
is_duplicate_dispatch() {
  local CMD=$1
  if [ ! -f "$DISPATCH_HISTORY" ]; then
    return 1  # No history = not duplicate
  fi
  local CMD_HASH=$(echo "$CMD" | head -c 80 | md5 2>/dev/null || echo "$CMD" | head -c 80 | md5sum 2>/dev/null | cut -d' ' -f1)
  local CUTOFF=$(( $(date +%s) - DEDUP_TTL ))
  # Check if this hash exists in recent history
  if awk -F'|' -v cutoff="$CUTOFF" -v hash="$CMD_HASH" '$1 >= cutoff && $3 == hash { found=1; exit } END { exit !found }' "$DISPATCH_HISTORY" 2>/dev/null; then
    return 0  # Duplicate found
  fi
  return 1  # Unique
}

# ═══════════════════════════════════════════════════════════════
# BRAIN EVOLUTION — Review learning state every 10 cycles
# Promotes high-ROI commands, demotes low-ROI ones
# ═══════════════════════════════════════════════════════════════
EVOLUTION_INTERVAL=10

evolve_brain() {
  echo "🧬 [EVOLUTION] Reviewing brain-learning-state.json..."
  timeout 10 node -e "
    try {
      const fs = require('fs');
      const roiCalc = require('$HOME/mekong-cli/apps/openclaw-worker/lib/factory-roi-calculator');
      const state = roiCalc.loadBrainState();
      const ce = state.commandEffectiveness || {};
      const entries = Object.entries(ce);
      if (entries.length < 3) { console.log('   Not enough data yet (' + entries.length + ' commands)'); return; }

      // Sort by success rate
      const sorted = entries.sort((a,b) => {
        const rA = a[1].total > 0 ? a[1].success / a[1].total : 0;
        const rB = b[1].total > 0 ? b[1].success / b[1].total : 0;
        return rB - rA;
      });

      // Log evolution insights
      const top = sorted.slice(0,3).map(([c,d]) => c.slice(0,30) + '(' + Math.round(d.success/d.total*100) + '%)');
      const bottom = sorted.slice(-2).filter(([,d]) => d.total > 1).map(([c,d]) => c.slice(0,30) + '(' + Math.round(d.success/d.total*100) + '%)');

      console.log('   Top: ' + top.join(', '));
      if (bottom.length) console.log('   Demote: ' + bottom.join(', '));

      // Record evolution event
      if (!state.evolutionLog) state.evolutionLog = [];
      state.evolutionLog.push({
        ts: new Date().toISOString(),
        commandsLearned: entries.length,
        topCommand: sorted[0] ? sorted[0][0].slice(0,40) : 'none',
        topRate: sorted[0] && sorted[0][1].total > 0 ? Math.round(sorted[0][1].success/sorted[0][1].total*100) : 0,
        worstCommand: sorted.length > 2 && sorted[sorted.length-1][1].total > 1 ? sorted[sorted.length-1][0].slice(0,40) : 'none',
      });
      // Keep last 50 evolution events
      if (state.evolutionLog.length > 50) state.evolutionLog = state.evolutionLog.slice(-50);

      roiCalc.saveBrainState(state);
      console.log('   Evolution logged (' + state.evolutionLog.length + ' events)');
    } catch(e) { console.log('   Evolution skip: ' + e.message); }
  " 2>/dev/null || true
  log_metric "brain_evolution" "complete" "0"
}

CYCLE_COUNT=0

while true; do
  CYCLE_COUNT=$((CYCLE_COUNT + 1))
  LOAD=$(sysctl -n vm.loadavg | awk '{print $2}')
  RAM=$(vm_stat | awk '/free/ {print $3}' | tr -d '.')
  echo "🧊 [$(date +%T)] Cycle=$CYCLE_COUNT Load=$LOAD RAM_free=$RAM"

  # HEALTH DASHBOARD: every 50 cycles
  if [ $((CYCLE_COUNT % 50)) -eq 0 ] && [ "$DRY_RUN" != true ]; then
    bash "$HOME/mekong-cli/scripts/cto-health-check.sh" 2>/dev/null || true
  fi

  # BRAIN EVOLUTION: review every 10 cycles
  if [ $((CYCLE_COUNT % EVOLUTION_INTERVAL)) -eq 0 ] && [ "$DRY_RUN" != true ]; then
    evolve_brain
  fi

  # ANOMALY DETECTION: check every 5 cycles
  if [ $((CYCLE_COUNT % 5)) -eq 0 ] && [ "$DRY_RUN" != true ]; then
    ANOMALY=$(timeout 5 node -e "
      try {
        const ad = require('$HOME/mekong-cli/apps/openclaw-worker/lib/cto-anomaly-detector');
        const r = ad.detectAnomalies();
        console.log(r.severity);
        if (r.anomalies.length > 0) r.anomalies.forEach(a => console.error('  ' + a.type + ': ' + a.message));
      } catch(e) { console.log('ok'); }
    " 2>&1)
    SEVERITY=$(echo "$ANOMALY" | head -1)
    if [ "$SEVERITY" = "critical" ]; then
      echo "🚨 ANOMALY CRITICAL — pausing dispatch for 1 cycle"
      echo "$ANOMALY" | tail -n +2
      log_metric "anomaly" "critical" "0"
      sleep $SLEEP_INTERVAL
      continue
    elif [ "$SEVERITY" = "warn" ]; then
      echo "⚠️ ANOMALY WARNING:"
      echo "$ANOMALY" | tail -n +2
      log_metric "anomaly" "warn" "0"
    fi
    # Generate telemetry snapshot
    timeout 5 node -e "
      try { require('$HOME/mekong-cli/apps/openclaw-worker/lib/cto-telemetry').generateTelemetry(); } catch(e) {}
    " 2>/dev/null || true
  fi

  # Periodic zombie cleanup (every cycle)
  pkill -f "node.*jest" 2>/dev/null || true
  pkill -f "node.*vitest" 2>/dev/null || true
  pkill -f "tsserver.js" 2>/dev/null || true

  # PRIORITY QUEUE: Sort panes by project state
  # Projects with code (src/) get dispatched before empty projects
  SORTED_PANES=()
  for i in "${!PANES[@]}"; do
    STATE=$(detect_project_state "${PANE_DIRS[$i]}" 2>/dev/null || echo "empty")
    case "$STATE" in
      "deployed")       SORTED_PANES=("$i" "${SORTED_PANES[@]}") ;;  # highest priority
      "scaffolded")     SORTED_PANES=("$i" "${SORTED_PANES[@]}") ;;
      "needs_install")  SORTED_PANES=("$i" "${SORTED_PANES[@]}") ;;
      *)                SORTED_PANES+=("$i") ;;  # empty = lowest priority
    esac
  done

  for i in "${SORTED_PANES[@]}"; do
    PANE=${PANES[$i]}
    PROJECT=${PANE_PROJECTS[$i]}
    DIR=${PANE_DIRS[$i]}
    NAME=${PANE_NAMES[$i]}

    # HEALTH CHECK — verify pane is alive before anything else (skip in dry-run)
    if [ "$DRY_RUN" = true ]; then
      # In dry-run, simulate idle pane and skip to dispatch
      echo "🧪 [P$PANE] DRY-RUN: simulating idle pane for $PROJECT"
      PROJ_STATE=$(detect_project_state "$DIR" 2>/dev/null || echo "empty")
      CASCADE_CMD=$(get_next_command "$PANE" "$PROJECT" "$DIR" "$NAME" 2>/dev/null || echo "/cook dry-run test")
      echo "   🔍 State: $PROJ_STATE → Command: $(echo "$CASCADE_CMD" | head -c 60)"
      log_metric "dispatch" "dry_sent" "0" "$PANE" "$PROJECT" "$CASCADE_CMD"
      continue
    fi
    if ! check_pane_health "$PANE"; then
      echo "🔄 [P$PANE] Attempting respawn..."
      tmux send-keys -t "$TMUX_SESSION:0.$PANE" -l "cd ~/mekong-cli && claude --dangerously-skip-permissions" 2>/dev/null || true
      sleep 0.5
      tmux send-keys -t "$TMUX_SESSION:0.$PANE" Enter 2>/dev/null || true
      log_metric "respawn" "triggered" "0" "$PANE" "$PROJECT"
      continue
    fi

    # TIMEOUT CHECK — kill hung commands >10 min
    if ! check_command_timeout "$PANE" "$PROJECT"; then
      mark_dispatch_done "$PANE"
      continue
    fi

    PANE_OUTPUT=$(tmux capture-pane -t "$TMUX_SESSION:0.$PANE" -p 2>/dev/null || echo "")
    LAST_45=$(echo "$PANE_OUTPUT" | tail -n 45)
    LAST_5=$(echo "$PANE_OUTPUT" | tail -n 5)

    # CRASHED
    if echo "$LAST_5" | grep -qE "bash-5|% $"; then
      echo "☠️ [P$PANE] CRASHED — Restarting CC CLI..."
      log_metric "crash" "detected" "0" "$PANE" "$PROJECT"
      tmux send-keys -t "$TMUX_SESSION:0.$PANE" -l "cd ~/mekong-cli && claude --dangerously-skip-permissions"
      sleep 0.5
      tmux send-keys -t "$TMUX_SESSION:0.$PANE" Enter
      mark_dispatch_done "$PANE"
      continue
    fi

    # QUEUED
    if echo "$LAST_5" | grep -qE "Press up to edit queued"; then
      echo "📬 [P$PANE] QUEUED — Clearing..."
      tmux send-keys -t "$TMUX_SESSION:0.$PANE" Escape
      sleep 1
      continue
    fi

    # WORKING — check LAST 15 lines (CC CLI shows prompt between tool calls)
    LAST_15=$(echo "$PANE_OUTPUT" | tail -n 15)
    if echo "$LAST_15" | grep -qE "Bash\(|Read [0-9]|Write\(|Edit\(|Running|thinking|Hashing|Blanching|Creating|Hatching|Puttering|Generating|Tempering|Crunching|Bloviating|Actioning|Manifesting|Stewing|Billowing|Cogitated|Dilly-dallying|Infusing|Churned|Sautéed|Composting|Baked|Warping|Newspapering|Prestidigitating|Channeling|Metamorphosing|Propagating|Scampering|Brewing|Frosting|Moonwalking|Concocting|Sautéing|Orbiting|Compacting|Ebbing|Pondering|Crystallizing|Precipitating|Mulling|Searching for|thought for|Harmonizing|auto-compact"; then
      echo "⚙️ [P$PANE] WORKING on $PROJECT — SKIP"
      continue
    fi

    # JUST FINISHED — mark previous command as done, log success
    if echo "$LAST_5" | grep -qE "✅ Done|✔|Sautéed for|Brewed for|Baked for|Cogitated for|Crunched for"; then
      # Calculate duration, classify output, and record learning
      if [ -f "/tmp/cto_dispatch_ts_P${PANE}" ]; then
        DISPATCH_TS=$(cat "/tmp/cto_dispatch_ts_P${PANE}" 2>/dev/null || echo "0")
        DURATION=$(( $(date +%s) - DISPATCH_TS ))
        LAST_CMD=$(cat "/tmp/cto_last_cmd_P${PANE}" 2>/dev/null || echo "unknown")
        LAST_STATE=$(cat "/tmp/cto_last_state_P${PANE}" 2>/dev/null || echo "deployed")

        # Save current output FIRST so intelligence reads fresh data
        save_pane_output "$PANE" "$LAST_45"

        # OUTPUT INTELLIGENCE: classify what the command actually produced
        OUTPUT_CLASS=$(timeout 5 node -e "
          try {
            const oi = require('$HOME/mekong-cli/apps/openclaw-worker/lib/output-intelligence');
            const output = require('fs').readFileSync('$PANE_STATE_DIR/pane_${PANE}_output', 'utf-8');
            const r = oi.classifyOutput(output);
            console.log(r.primary + '|' + oi.summarize(output));
          } catch(e) { console.log('unknown|error'); }
        " 2>/dev/null || echo "unknown|error")

        OUTPUT_TYPE=$(echo "$OUTPUT_CLASS" | cut -d'|' -f1)
        OUTPUT_SUMMARY=$(echo "$OUTPUT_CLASS" | cut -d'|' -f2)
        echo "   🧠 Output: ${OUTPUT_TYPE} [${OUTPUT_SUMMARY}]"

        log_metric "command_complete" "$OUTPUT_TYPE" "$DURATION" "$PANE" "$PROJECT" "$LAST_CMD"
        record_outcome "$PROJECT" "$LAST_STATE" "$LAST_CMD" "true" "$DURATION"
        mark_dispatch_done "$PANE"
      fi

      if echo "$LAST_5" | grep -qE "❯"; then
        : # idle — fall through to dispatch
      else
        echo "🏁 [P$PANE] JUST FINISHED — cooldown"
        continue
      fi
    fi

    # TRULY IDLE → dispatch command with cascade logic
    if echo "$LAST_5" | grep -qE "❯|bypass permissions"; then
      # GUARD: if dispatch timestamp exists and < 10min old, pane may still be processing
      if [ -f "/tmp/cto_dispatch_ts_P${PANE}" ]; then
        ACTIVE_TS=$(cat "/tmp/cto_dispatch_ts_P${PANE}" 2>/dev/null || echo "0")
        ACTIVE_AGE=$(( $(date +%s) - ACTIVE_TS ))
        if [ "$ACTIVE_AGE" -lt "$COMMAND_TIMEOUT" ]; then
          echo "🔒 [P$PANE] DISPATCH LOCK: task sent ${ACTIVE_AGE}s ago, waiting for completion"
          continue
        fi
      fi
      mark_dispatch_done "$PANE"  # Clear stale dispatch timer

      COOLDOWN_FILE="/tmp/cto_cooldown_P${PANE}"
      NOW=$(date +%s)
      LAST_DISPATCH=$(cat "$COOLDOWN_FILE" 2>/dev/null || echo "0")
      ELAPSED=$((NOW - LAST_DISPATCH))

      if [ "$ELAPSED" -lt 180 ]; then
        echo "⏳ [P$PANE] COOLDOWN (${ELAPSED}s < 180s) — skip"
        continue
      fi

      # Save current output for chaining (A→B)
      save_pane_output "$PANE" "$LAST_45"

      # CASCADE LOGIC: analyze + get command
      CASCADE_CMD=$(get_cascade_command "$PANE" "$PROJECT" "$DIR" "$NAME" "$LAST_45" 2>/dev/null)

      # DEDUP CHECK: prevent same command dispatched to multiple panes
      if is_duplicate_dispatch "$CASCADE_CMD"; then
        echo "🔄 [P$PANE] DEDUP: command already dispatched to another pane — skipping"
        log_metric "dedup_skip" "duplicate" "0" "$PANE" "$PROJECT" "$CASCADE_CMD"
        continue
      fi

      # Save context file for CC CLI to reference
      echo "$LAST_45" | grep -v "^$" | grep -v "^─" | grep -v "bypass" | grep -v "qwen" | tail -15 > "/tmp/cto_context_P${PANE}.txt"

      # Determine layer
      LAYER="?"
      case "$CASCADE_CMD" in
        /raas*) LAYER="🏗️ Scaffold" ;;
        /studio*|/portfolio*) LAYER="🏯 Studio" ;;
        /founder*|/venture*) LAYER="👑 Founder" ;;
        /business*|/sales*|/marketing*) LAYER="💼 Business" ;;
        /plan*|/design*) LAYER="🎯 Product" ;;
        /cook*) LAYER="⚙️ Engineering" ;;
        *) LAYER="🏯 Studio" ;;
      esac

      echo "$LAYER [P$PANE] CASCADE DISPATCH for $PROJECT:"
      echo "   📌 $CASCADE_CMD"

      # DISPATCH + start timeout timer + save for learning
      if [ "$DRY_RUN" = true ]; then
        echo "   🧪 DRY-RUN: would send → $CASCADE_CMD"
      else
        tmux send-keys -t "$TMUX_SESSION:0.$PANE" -l "$CASCADE_CMD"
        sleep 0.5
        tmux send-keys -t "$TMUX_SESSION:0.$PANE" Enter
      fi
      echo "$NOW" > "$COOLDOWN_FILE"
      mark_dispatch_start "$PANE"
      record_dispatch "$PANE" "$CASCADE_CMD"
      echo "$CASCADE_CMD" | head -c 60 > "/tmp/cto_last_cmd_P${PANE}"
      detect_project_state "$DIR" > "/tmp/cto_last_state_P${PANE}" 2>/dev/null || true
      log_metric "dispatch" "${DRY_RUN:+dry_}sent" "0" "$PANE" "$PROJECT" "$CASCADE_CMD"
      continue
    fi

    echo "❓ [P$PANE] UNKNOWN STATE"
    log_metric "unknown_state" "detected" "0" "$PANE" "$PROJECT"
  done

  # Cycle limit check (for --cycles=N flag)
  if [ "$MAX_CYCLES" -gt 0 ] && [ "$CYCLE_COUNT" -ge "$MAX_CYCLES" ]; then
    echo "🏁 Reached max cycles ($MAX_CYCLES) — exiting"
    break
  fi

  echo "💤 [$(date +%T)] Sleeping ${SLEEP_INTERVAL}s..."
  sleep $SLEEP_INTERVAL
done
