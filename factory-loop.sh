# ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
record_outcome() {
  local PROJECT=$1 STATE=$2 CMD=$3 SUCCESS=$4 DURATION=$5
  timeout 5 node -e "
    try {
      const r = require('$HOME/mekong-cli/apps/openclaw-worker/lib/factory-roi-calculator');
      r.recordCommandOutcome('$PROJECT', '$STATE', '$(echo "$CMD" | head -c 60)', $SUCCESS, $DURATION);
    } catch(e) {}
  " 2>/dev/null || true
}

# Check if a command should be avoided based on past failures
should_avoid() {
  local PROJECT=$1 STATE=$2 CMD=$3
  timeout 3 node -e "
    try {
      const r = require('$HOME/mekong-cli/apps/openclaw-worker/lib/factory-roi-calculator');
      process.exit(r.shouldAvoidCommand('$PROJECT', '$STATE', '$(echo "$CMD" | head -c 60)') ? 0 : 1);
    } catch(e) { process.exit(1); }
  " 2>/dev/null
}

CYCLE_COUNT=0

while true; do
  CYCLE_COUNT=$((CYCLE_COUNT + 1))
  LOAD=$(sysctl -n vm.loadavg | awk '{print $2}')
  RAM=$(vm_stat | awk '/free/ {print $3}' | tr -d '.')
  echo "üßä [$(date +%T)] Cycle=$CYCLE_COUNT Load=$LOAD RAM_free=$RAM"

  # Periodic zombie cleanup (every cycle)
  pkill -f "node.*jest" 2>/dev/null || true
  pkill -f "node.*vitest" 2>/dev/null || true
  pkill -f "tsserver.js" 2>/dev/null || true

  # PRIORITY QUEUE: Sort panes by project state
  # Projects with code (src/) get dispatched before empty projects
  SORTED_PANES=()
  for i in "${!PANES[@]}"; do
    STATE=$(detect_project_state "${PANE_DIRS[$i]}" 2>/dev/null || echo "empty")
    case "$STATE" in
      "deployed")       SORTED_PANES=("$i" "${SORTED_PANES[@]}") ;;  # highest priority
      "scaffolded")     SORTED_PANES=("$i" "${SORTED_PANES[@]}") ;;
      "needs_install")  SORTED_PANES=("$i" "${SORTED_PANES[@]}") ;;
      *)                SORTED_PANES+=("$i") ;;  # empty = lowest priority
    esac
  done

  for i in "${SORTED_PANES[@]}"; do
    PANE=${PANES[$i]}
    PROJECT=${PANE_PROJECTS[$i]}
    DIR=${PANE_DIRS[$i]}
    NAME=${PANE_NAMES[$i]}

    # HEALTH CHECK ‚Äî verify pane is alive before anything else
    if ! check_pane_health "$PANE"; then
      echo "üîÑ [P$PANE] Attempting respawn..."
      tmux send-keys -t "$TMUX_SESSION:0.$PANE" -l "cd ~/mekong-cli && claude --dangerously-skip-permissions" 2>/dev/null || true
      sleep 0.5
      tmux send-keys -t "$TMUX_SESSION:0.$PANE" Enter 2>/dev/null || true
      log_metric "respawn" "triggered" "0" "$PANE" "$PROJECT"
      continue
    fi

    # TIMEOUT CHECK ‚Äî kill hung commands >10 min
    if ! check_command_timeout "$PANE" "$PROJECT"; then
      mark_dispatch_done "$PANE"
      continue
    fi

    PANE_OUTPUT=$(tmux capture-pane -t "$TMUX_SESSION:0.$PANE" -p 2>/dev/null || echo "")
    LAST_45=$(echo "$PANE_OUTPUT" | tail -n 45)
    LAST_5=$(echo "$PANE_OUTPUT" | tail -n 5)

    # CRASHED
    if echo "$LAST_5" | grep -qE "bash-5|% $"; then
      echo "‚òÝÔ∏è [P$PANE] CRASHED ‚Äî Restarting CC CLI..."
      log_metric "crash" "detected" "0" "$PANE" "$PROJECT"
      tmux send-keys -t "$TMUX_SESSION:0.$PANE" -l "cd ~/mekong-cli && claude --dangerously-skip-permissions"
      sleep 0.5
      tmux send-keys -t "$TMUX_SESSION:0.$PANE" Enter
      mark_dispatch_done "$PANE"
      continue
    fi

    # QUEUED
    if echo "$LAST_5" | grep -qE "Press up to edit queued"; then
      echo "üì¨ [P$PANE] QUEUED ‚Äî Clearing..."
      tmux send-keys -t "$TMUX_SESSION:0.$PANE" Escape
      sleep 1
      continue
    fi

    # WORKING ‚Äî check LAST 5 lines ONLY (active animation near prompt)
    if echo "$LAST_5" | grep -qE "Bash\(|Read [0-9]|Write\(|Edit\(|Running|thinking|Hashing|Blanching|Creating|Hatching|Puttering|Generating|Tempering|Crunching|Bloviating|Actioning|Manifesting|Stewing|Billowing|Cogitated|Dilly-dallying|Infusing|Churned|Saut√©ed|Composting|Baked|Warping|Newspapering|Prestidigitating|Channeling|Metamorphosing|Propagating|Scampering|Brewing|Frosting|Moonwalking|Concocting|Saut√©ing|Orbiting|Compacting|Ebbing|Pondering|Crystallizing|Precipitating|Mulling|Searching for|thought for|Harmonizing"; then
      echo "‚öôÔ∏è [P$PANE] WORKING on $PROJECT ‚Äî SKIP"
      continue
    fi

    # JUST FINISHED ‚Äî mark previous command as done, log success
    if echo "$LAST_5" | grep -qE "‚úÖ Done|‚úî|Saut√©ed for|Brewed for|Baked for|Cogitated for|Crunched for"; then
      # Calculate duration and record learning outcome
      if [ -f "/tmp/cto_dispatch_ts_P${PANE}" ]; then
        DISPATCH_TS=$(cat "/tmp/cto_dispatch_ts_P${PANE}" 2>/dev/null || echo "0")
        DURATION=$(( $(date +%s) - DISPATCH_TS ))
        log_metric "command_complete" "success" "$DURATION" "$PANE" "$PROJECT"
        LAST_CMD=$(cat "/tmp/cto_last_cmd_P${PANE}" 2>/dev/null || echo "unknown")
        LAST_STATE=$(cat "/tmp/cto_last_state_P${PANE}" 2>/dev/null || echo "deployed")
        record_outcome "$PROJECT" "$LAST_STATE" "$LAST_CMD" "true" "$DURATION"
        mark_dispatch_done "$PANE"
      fi

      if echo "$LAST_5" | grep -qE "‚ùØ"; then
        : # idle ‚Äî fall through to dispatch
      else
        echo "üèÅ [P$PANE] JUST FINISHED ‚Äî cooldown"
        continue
      fi
    fi

    # TRULY IDLE ‚Üí dispatch command with cascade logic
    if echo "$LAST_5" | grep -qE "‚ùØ|bypass permissions"; then
      mark_dispatch_done "$PANE"  # Clear any stale dispatch timer

      COOLDOWN_FILE="/tmp/cto_cooldown_P${PANE}"
      NOW=$(date +%s)
      LAST_DISPATCH=$(cat "$COOLDOWN_FILE" 2>/dev/null || echo "0")
      ELAPSED=$((NOW - LAST_DISPATCH))

      if [ "$ELAPSED" -lt 180 ]; then
        echo "‚è≥ [P$PANE] COOLDOWN (${ELAPSED}s < 180s) ‚Äî skip"
        continue
      fi

      # Save current output for chaining (A‚ÜíB)
      save_pane_output "$PANE" "$LAST_45"

      # CASCADE LOGIC: analyze + get command
      CASCADE_CMD=$(get_cascade_command "$PANE" "$PROJECT" "$DIR" "$NAME" "$LAST_45" 2>/dev/null)

      # Save context file for CC CLI to reference
      echo "$LAST_45" | grep -v "^$" | grep -v "^‚îÄ" | grep -v "bypass" | grep -v "qwen" | tail -15 > "/tmp/cto_context_P${PANE}.txt"

      # Determine layer
      LAYER="?"
      case "$CASCADE_CMD" in
        /raas*) LAYER="üèóÔ∏è Scaffold" ;;
        /studio*|/portfolio*) LAYER="üèØ Studio" ;;
        /founder*|/venture*) LAYER="üëë Founder" ;;
        /business*|/sales*|/marketing*) LAYER="üíº Business" ;;
        /plan*|/design*) LAYER="üéØ Product" ;;
        /cook*) LAYER="‚öôÔ∏è Engineering" ;;
        *) LAYER="üèØ Studio" ;;
      esac

      echo "$LAYER [P$PANE] CASCADE DISPATCH for $PROJECT:"
      echo "   üìå $CASCADE_CMD"

      # DISPATCH + start timeout timer + save for learning
      tmux send-keys -t "$TMUX_SESSION:0.$PANE" -l "$CASCADE_CMD"
      sleep 0.5
      tmux send-keys -t "$TMUX_SESSION:0.$PANE" Enter
      echo "$NOW" > "$COOLDOWN_FILE"
      mark_dispatch_start "$PANE"
      echo "$CASCADE_CMD" | head -c 60 > "/tmp/cto_last_cmd_P${PANE}"
      detect_project_state "$DIR" > "/tmp/cto_last_state_P${PANE}" 2>/dev/null || true
      log_metric "dispatch" "sent" "0" "$PANE" "$PROJECT" "$CASCADE_CMD"
      continue
    fi

    echo "‚ùì [P$PANE] UNKNOWN STATE"
    log_metric "unknown_state" "detected" "0" "$PANE" "$PROJECT"
  done

  echo "üí§ [$(date +%T)] Sleeping ${SLEEP_INTERVAL}s..."
  sleep $SLEEP_INTERVAL
done
