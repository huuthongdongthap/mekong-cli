# ═══════════════════════════════════════════════════════════════
# TRAP + CLEANUP — Kill zombie processes on exit
# ═══════════════════════════════════════════════════════════════
cleanup() {
  echo "🧹 [$(date +%T)] FACTORY SHUTDOWN — cleaning up zombies..."
  pkill -f "node.*jest" 2>/dev/null || true
  pkill -f "node.*vitest" 2>/dev/null || true
