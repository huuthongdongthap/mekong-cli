# ═══════════════════════════════════════════════════════════════
# DAILY DIGEST — Summarize factory activity to reports/
# ═══════════════════════════════════════════════════════════════
write_daily_digest() {
  local TODAY=$(date +%y%m%d)
  local DIGEST_FILE="${DAILY_DIGEST_DIR}/factory-daily-${TODAY}.md"
  mkdir -p "$DAILY_DIGEST_DIR"

  if [ ! -f "$METRICS_LOG" ]; then
    return
  fi

  local TODAY_ISO=$(date +%Y-%m-%d)
  local TODAY_LINES=$(grep "^${TODAY_ISO}" "$METRICS_LOG" 2>/dev/null || true)

  if [ -z "$TODAY_LINES" ]; then
    return
  fi

  local DISPATCHES=$(echo "$TODAY_LINES" | grep -c "dispatch" 2>/dev/null || true)
  local SUCCESSES=$(echo "$TODAY_LINES" | grep -c "command_complete.*success" 2>/dev/null || true)
  local TIMEOUTS=$(echo "$TODAY_LINES" | grep -c "command_timeout" 2>/dev/null || true)
  local CRASHES=$(echo "$TODAY_LINES" | grep -c "crash" 2>/dev/null || true)
  local UNKNOWNS=$(echo "$TODAY_LINES" | grep -c "unknown_state" 2>/dev/null || true)
  DISPATCHES=${DISPATCHES:-0}; SUCCESSES=${SUCCESSES:-0}; TIMEOUTS=${TIMEOUTS:-0}
  CRASHES=${CRASHES:-0}; UNKNOWNS=${UNKNOWNS:-0}

  cat > "$DIGEST_FILE" << DIGEST
# Factory Daily Digest — ${TODAY_ISO}

## Summary
- Dispatches: ${DISPATCHES}
- Successes: ${SUCCESSES}
- Timeouts: ${TIMEOUTS}
- Crashes: ${CRASHES}
- Unknown states: ${UNKNOWNS}
- Success rate: $([ "$DISPATCHES" -gt 0 ] && echo "$((SUCCESSES * 100 / DISPATCHES))%" || echo "N/A")

## Per-Project
$(echo "$TODAY_LINES" | grep "dispatch" | awk -F'|' '{gsub(/^ +| +$/,"",$4); print $4}' | sort | uniq -c | sort -rn | head -5 | while read count proj; do echo "- ${proj}: ${count} dispatches"; done)

## Last 10 Events
$(echo "$TODAY_LINES" | tail -10 | awk -F'|' '{gsub(/^ +| +$/,"",$1); gsub(/^ +| +$/,"",$2); gsub(/^ +| +$/,"",$4); printf "- %s %s %s\n", $1, $2, $4}')

---
*Auto-generated by factory-loop.sh v13.1*
DIGEST

  echo "📋 Daily digest written: $DIGEST_FILE"
}

# ═══════════════════════════════════════════════════════════════
# METRICS LOGGING — Append to /tmp/factory-metrics.log
# Format: ISO_TIMESTAMP | event | pane | project | status | duration_s | command
# ═══════════════════════════════════════════════════════════════
log_metric() {
  local EVENT=$1 STATUS=$2 DURATION=${3:-0} PANE=${4:-"-"} PROJECT=${5:-"-"} CMD=${6:-"-"}
  echo "$(date -u +%Y-%m-%dT%H:%M:%SZ) | ${EVENT} | P${PANE} | ${PROJECT} | ${STATUS} | ${DURATION}s | ${CMD:0:80}" >> "$METRICS_LOG"
}

# ═══════════════════════════════════════════════════════════════
# HEALTH CHECK — Verify pane is responsive before dispatch
# Returns 0 if healthy, 1 if unhealthy
# ═══════════════════════════════════════════════════════════════
check_pane_health() {
  local PANE=$1

  # 1. Verify tmux pane exists
  if ! tmux has-session -t "$TMUX_SESSION" 2>/dev/null; then
    echo "💀 [P$PANE] tmux session '$TMUX_SESSION' not found"
    log_metric "health_check" "session_dead" "0" "$PANE"
    return 1
  fi

  # 2. Verify pane is alive (can capture output)
  local CAPTURE
  CAPTURE=$(tmux capture-pane -t "$TMUX_SESSION:0.$PANE" -p 2>/dev/null) || {
    echo "💀 [P$PANE] pane capture failed — pane dead"
    log_metric "health_check" "pane_dead" "0" "$PANE"
    return 1
  }

  # 3. Check for known fatal states
  local TAIL3=$(echo "$CAPTURE" | tail -n 3)
  if echo "$TAIL3" | grep -qE "Segmentation fault|killed|SIGKILL|core dumped"; then
    echo "💀 [P$PANE] fatal crash detected"
    log_metric "health_check" "fatal_crash" "0" "$PANE"
    return 1
  fi

  return 0
}

# ═══════════════════════════════════════════════════════════════
# HUNG COMMAND DETECTION — Kill commands stuck >10 min
# ═══════════════════════════════════════════════════════════════
check_command_timeout() {
  local PANE=$1 PROJECT=$2
  local TS_FILE="/tmp/cto_dispatch_ts_P${PANE}"
  local NOW=$(date +%s)

  if [ ! -f "$TS_FILE" ]; then
    return 0  # No active command
  fi

  local DISPATCH_TS=$(cat "$TS_FILE" 2>/dev/null || echo "0")
  local ELAPSED=$((NOW - DISPATCH_TS))

  if [ "$ELAPSED" -gt "$COMMAND_TIMEOUT" ]; then
    echo "⏰ [P$PANE] TIMEOUT: command hung for ${ELAPSED}s (>${COMMAND_TIMEOUT}s) — sending Escape+Enter"
    log_metric "command_timeout" "hung" "$ELAPSED" "$PANE" "$PROJECT"
    # Send Escape to cancel, then Enter to get back to prompt
    tmux send-keys -t "$TMUX_SESSION:0.$PANE" Escape
    sleep 1
    tmux send-keys -t "$TMUX_SESSION:0.$PANE" Enter
    rm -f "$TS_FILE"
    return 1
  fi

  return 0
}

